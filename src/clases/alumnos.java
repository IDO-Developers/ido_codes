package clases;

public class alumnos {

	int id;
	int Id_Prematricula;
	String RNE_Alumno;
	String Id_Rol;
	String password;
	String Id_Grupo;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getId_Prematricula() {
		return Id_Prematricula;
	}

	public void setId_Prematricula(int id_Prematricula) {
		Id_Prematricula = id_Prematricula;
	}

	public String getRNE_Alumno() {
		return RNE_Alumno;
	}

	public void setRNE_Alumno(String rNE_Alumno) {
		RNE_Alumno = rNE_Alumno;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getId_Grupo() {
		return Id_Grupo;
	}

	public void setId_Grupo(String id_Grupo) {
		Id_Grupo = id_Grupo;
	}

	public String getId_Rol() {
		return Id_Rol;
	}

	public void setId_Rol(String id_Rol) {
		Id_Rol = id_Rol;
	}

}
